<script>
	export default {
		data() {
			return {
				options: [],
				target: 1,
				money: null,
				showTradeName: '',
				showTradeInfo: '',
				modalName: '',
				signOrderid: '',
				smsCode: '',
				bgColor: ''
			}
		},
		onLoad(e) {
			if(e.type && e.bgColor) {
				// 获取类型，0：智能养卡，1：快捷支付。
				this.target = e.type
				// 获取卡片颜色
				this.bgColor = e.bgColor
				// 设置标题
				let pageTitle = ''
				if(this.target == 0) {
					pageTitle = '智能养卡'
				}
				if(this.target == 1) {
					pageTitle = '快捷支付'
				}
				uni.setNavigationBarTitle({
					title: pageTitle
				})
				console.log(this.target)
			}
			uni.$on("card", (data) => {
				console.log("接收事件card成功,data=", data);
				this.options = []
				this.options.push(data.data)
			});
			
		},
		methods: {
			hideModal(e) {
				this.modalName = null
			},
			jhconfirm(){
				//确认绑卡
				this.$queue.showLoading("请稍后...");
				this.$Request.postP('/pay/bankSignConfirm',{
					    "trade_id": this.trade_id, //通道id
					    "bank_id": this.bank_id ,//银行卡id 
						"signOrderid": this.signOrderid,
						"smsCode":this.smsCode,
				}).then(res => {
					//console.log(res);
					uni.hideLoading();
					if (res.status === 10000) { 
						uni.showToast({
							title: '激活成功！',
							icon: 'none'
						});
						this.modalName = null;
					}else{
						uni.showToast({
							title: res.msg,
							icon: 'none'
						});
					} 
				});
				uni.hideLoading();
			},
			ShowTradeInfo(id){
				console.log(id);
				this.$Request.postP('/trade/detail',{
					"trade_id": id
				}).then(res => {
					console.log(res.data.show_name);
					this.showTradeName = res.data.show_name
					this.showTradeInfo = res.data.des;
					// this.options = res.data;
					// console.log(this.options);
				});
				
				
				this.modalName = "Image";
			},
			addPlan(planid,cardid){
				uni.navigateTo({
					url:'../daihuan/daihuan?planid='+planid+'&cardid='+cardid
				});
			},
			jihuo(planid,cardid){
				//this.$queue.showLoading("正在发送验证码...");
				this.trade_id =planid;
				this.bank_id = cardid;
				this.$Request.postP('/pay/bankSign',{
					    "trade_id": planid, //通道id
					    "bank_id": cardid //银行卡id
				}).then(res => {
					//console.log(res);
					if (res.status === 10000) {
						console.log('is_sign_auto')
						if(res.data.is_sign_auto == 1){
							uni.showToast({
								title: '激活通道成功！',
								icon: 'none'
							});
							// uni.redirectTo({
							//     'url':'/pages/member/mycard'
							// });
							return;
						}
						//console.log(res.data)
						this.signOrderid = res.data.hnapayOrderId;
						//console.log(this.signOrderid)
						uni.showToast({
							title: '短信发送成功！',
							icon: 'none'
						});
						this.modalName ='DialogModal1';
					}else{
						uni.showToast({
							title: res.msg,
							icon: 'none'
						});
					}
			
				});
				
				///发送验证码
			},
			lookPlan(tradeid,cardid){
				uni.navigateTo({
					url:'../daihuan/lookplan?tradeid='+tradeid+'&cardid='+cardid
				});
			},
			easyPayCreate(trade_id,bank_id){
				//快捷支付
				console.log(this.money)
				if(this.money <= 0){
					uni.showToast({
						title: '请输入消费金额！',
						icon: 'none'
					});
					return; 
				}
				this.$queue.showLoading("请稍后...");
				this.$Request.postP('/easyPayCreate',{
					    "trade_id": trade_id, //通道id
					    "bank_id": bank_id ,//银行卡id 
						"pay_type": "快捷",
						"money": this.money,
				}).then(res => {
					console.log(res);
					if (res.status === 10000) { 
						 uni.navigateTo({
						 	url: '/pages/member/kuaijie?url=' + res.data.url
						 });
					}else{
						 uni.showToast({
						 	title: res.msg,
						 	icon: 'none'
						 });
					}
				});
				uni.hideLoading();
			},
		}
	}
</script>

